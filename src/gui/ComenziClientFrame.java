    public void afisareComenzi(){
        List<ComandaDB> comenzi = MainController.getInstance().getComenziByClient(client);
        model1.clear();
        for(ComandaDB c: comenzi){
            model1.addElement(c);
        }
    }
    
    public void afisareCarti(ComandaDB comanda){
        List<CarteDB> carti = MainController.getInstance().getCartiByComanda(comanda);
        model2.clear();
        for(CarteDB c: carti){
                  model2.addElement(c);
        }
    }

