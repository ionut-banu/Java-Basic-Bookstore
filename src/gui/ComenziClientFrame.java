    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        if(evt.getButton() == MouseEvent.BUTTON3 &&
                jList1.getSelectedIndex() > -1){
            jPopupMenu1.show(jList1, evt.getX(), evt.getY());
        }
        
        if(evt.getButton() == MouseEvent.BUTTON1 &&
                jList1.getSelectedIndex() > -1){
            ComandaDB comanda = (ComandaDB)jList1.getSelectedValue();
            afisareCarti(comanda);
            comanda.setTotal(MainController.getInstance().getTotal(comanda));
            MainController.getInstance().modificaComanda(comanda);
            jTextField2.setText(MainController.getInstance().getTotal(comanda)+" ");
            
            if(comanda.getStatus().equalsIgnoreCase("IN DESFASURARE")){
                jButton2.setEnabled(true);
                jButton2.setText("Marcheaza ca EFECTUATA");
            }else{
                jButton2.setEnabled(true);
                jButton2.setText("Marcheaza ca IN DESFASURARE");
            }
        }
    }//GEN-LAST:event_jList1MouseClicked

    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        ComandaDB comanda = (ComandaDB) jList1.getSelectedValue();
        if(comanda.getStatus() == "IN DESFASURARE"){
            new AdaugaCarteComandaFrame(comanda,this).setVisible(true);
        }else{
            JOptionPane.showMessageDialog(null, "Comanda a fost efectuata. Nu mai pot fi adaugate carti!");
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

    private void jList2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList2MouseClicked
        if(evt.getButton() == MouseEvent.BUTTON1 &&
                jList2.getSelectedIndex() > -1 &&
                jList1.getSelectedIndex() > -1){
            ComandaDB comanda = (ComandaDB) jList1.getSelectedValue();
            CarteDB carte = (CarteDB) jList2.getSelectedValue();
            int cantitate = MainController.getInstance().getCantitate(comanda, carte);
            jTextField1.setText(" "+cantitate+" ");
        }
    }//GEN-LAST:event_jList2MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(jList1.getSelectedIndex() > -1){
            ComandaDB comanda = (ComandaDB) jList1.getSelectedValue();
            if(comanda.getStatus().equalsIgnoreCase("IN DESFASURARE")){
                comanda.setStatus("EFECTUATA");
                jButton2.setText("Marcheaza ca IN DESFASURARE");
                MainController.getInstance().modificaComanda(comanda);
                afisareComenzi();
            }else{
                comanda.setStatus("IN DESFASURARE");
                jButton2.setText("Marcheaza ca EFECTUATA");
                MainController.getInstance().modificaComanda(comanda);
                afisareComenzi();
            }
        jButton2.setEnabled(false);
        jButton2.setText("Modifica status");
        }
    }//GEN-LAST:event_jButton2ActionPerformed



