    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String titlu = jTextField1.getText();
        String isbn = jTextField2.getText();
        int stoc = Integer.parseInt(jTextField3.getText());
        editurac = (EdituraDB)jList2.getSelectedValue();
        furnizorc = (FurnizorDB)jList3.getSelectedValue();
        double pret = Double.parseDouble(jTextField6.getText());
        
        
        boolean rez = MainController.getInstance().adaugaCarte(titlu, isbn, stoc, pret, editurac, furnizorc, autoric);
        if(rez==true){
            JOptionPane.showMessageDialog(null, "Cartea a fost adaugata!");
            cf.afisareCarti();
            dispose();
        }else{
            JOptionPane.showMessageDialog(null, "O carte cu acelasi ISBN exista deja!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

