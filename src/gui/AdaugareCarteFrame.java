    public void afisareEdituri(){
        model2.clear();
        List<EdituraDB> edituri = MainController.getInstance().getEdituri();
        for(EdituraDB e: edituri){
            model2.addElement(e);
        }
    }
    
    public void afisareFurnizori(){
        model3.clear();
        List<FurnizorDB> furnizori = MainController.getInstance().getFurnizori();
        for(FurnizorDB f: furnizori){
            model3.addElement(f);
        }
    }
    
    public void afisareAutori(){
        int ok=1;
        model4.clear();
        List<AutorDB> autori = MainController.getInstance().getAutori();
        for(AutorDB a: autori){
            ok = 1;
            for(AutorDB ac: autoric){
                if(a.getId()==ac.getId()){
                    ok=0; 
                }     
            }
            if(ok==1) model4.addElement(a);
        }
    }
    
    public void afisareAutoriC(){
        model1.clear();
        for(AutorDB ac: autoric){
            model1.addElement(ac);
        }
    }

